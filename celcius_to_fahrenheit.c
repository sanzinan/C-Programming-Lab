//Convert Celsius to Fahrenheit
#include<stdio.h>
int main ()
{
    float fahrenheit, celsius;

    printf("Enter the value of Celsius = ", celsius);
    scanf("%f", & celsius);


    fahrenheit = ((1.8 * celsius) + 32 );
    printf("Fahrenheit value is = %.2f ", fahrenheit);


    return 0 ;

}

